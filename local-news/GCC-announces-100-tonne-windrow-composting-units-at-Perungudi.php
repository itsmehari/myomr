            <h1 class="news-headline">Perungudi to Get Major Wet Waste Composting Facility: GCC Announces ₹13.6 Crore Project</h1>
            <div class="news-byline">Perungudi, Chennai – May 14, 2025</div>
            <div class="news-main-img-card">
                <img src="/local-news/omr-news-images/GCC-announces-100-tonne-windrow-composting-units-at-Perungudi.webp" alt="Aerial view of Perungudi landfill, site of new GCC composting facility">
            </div>
            <div class="news-section">
                <p>In a significant development for waste management in the OMR corridor, the Greater Chennai Corporation (GCC) has announced the establishment of two state-of-the-art Windrow composting facilities at the Perungudi landfill, aimed at processing up to 100 tonnes of wet waste per day. This move is part of the Corporation's continued efforts to decentralize solid waste processing and reduce the environmental burden on Chennai's landfills.</p>
            </div>
            <div class="news-section">
                <div class="news-section-title">♻️ Project Highlights</div>
                <ul>
                    <li><strong>Total estimated cost:</strong> ₹13.6 crore</li>
                    <li><strong>Facilities include:</strong>
                        <ul>
                            <li>Open cement concrete platforms for storing wet waste</li>
                            <li>GI sheet-roofed processing buildings</li>
                            <li>Advanced machinery: 2 wet waste shredders (8 MT/hr), 2 conveyor systems, compost turners (5 MT/hr)</li>
                        </ul>
                    </li>
                    <li>The two individual units have been budgeted at ₹6.84 crore and ₹6.77 crore, respectively.</li>
                    <li>Will serve Teynampet to Sholinganallur zones (Zones 9 to 15), including Perungudi, Thoraipakkam, Karapakkam, and Sholinganallur.</li>
                </ul>
            </div>
            <div class="news-section">
                <div class="news-section-title">🛠️ Why It Matters for OMR</div>
                <ul>
                    <li>Reduces long-distance transportation of waste</li>
                    <li>Lowers processing burden on central landfills</li>
                    <li>Decreases air and soil pollution</li>
                    <li>Improves local hygiene and reduces vector-borne disease risk</li>
                    <li>Enhances quality of life for residents in Perungudi, Sholinganallur, Thuraipakkam, and nearby localities</li>
                </ul>
                <p>With OMR being one of Chennai's fastest-growing urban corridors, the daily generation of household and commercial waste is substantial. According to GCC, Chennai produces 6,150 metric tonnes of solid waste daily, including 3,200 MT of wet waste. Managing this effectively at the zone level is crucial for a sustainable future.</p>
            </div>
            <div class="news-section">
                <div class="news-section-title">👥 Local Feedback</div>
                <p>While the initiative has received praise from urban planners and sustainability advocates, residents in Manali, where similar composting units are already in place, have raised concerns about odour and health issues, particularly respiratory problems. GCC officials have acknowledged these reports and assured that the new Perungudi facilities will include enhanced ventilation and odour control systems.</p>
                <div class="news-quote">“With advanced composting machinery and a better zonal planning model, these units will serve as a benchmark in sustainable waste processing,” said K. Vijayakumar, Chief Engineer, Solid Waste Management (SWM), GCC.</div>
