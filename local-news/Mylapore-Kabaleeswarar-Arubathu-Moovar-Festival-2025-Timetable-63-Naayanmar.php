  <h2>Mylapore Panguni Festival 2025 – Sri Kapaleeshwarar Temple Celebrations (Panguni Peruvizha Schedule)</h2>
  <p>The annual Panguni Peruvizha at Mylapore's Sri Kapaleeshwarar Temple is set to take place from April 3 to April 12, 2025. The detailed event calendar is as follows:</p>

  <h4>April 2, 2025 – Wednesday</h4>
  <ul>
    <li>Devotional offerings to Grama Devatha Sri Kolavizhi Amman</li>
    <li>Rituals: Mrithsangrihanam and Angurarpanam</li>
    <li>9:00 PM – Procession of Sri Narthana Vinayagar on the Silver Mouse Vahanam</li>
  </ul>

  <h4>April 3, 2025 – Thursday</h4>
  <ul>
    <li>7:35 AM – Arrival of the deity at the Kodiyetra Mandapam</li>
    <li>8:10 AM – 8:35 AM – Flag hoisting ceremony (Kodiyetram) on the Velli Pavazhakaal Vimanam</li>
    <li>10:00 PM – Ambal's Mayil Vahanam (Peacock Mount) and divine Siva Puja Darshan</li>
  </ul>

  <h4>April 4, 2025 – Friday</h4>
  <ul>
    <li>8:30 AM – Suriya Vattam (Sun Circle)</li>
    <li>9:00 PM – Chandira Vattam (Moon Circle)</li>
  </ul>

  <h4>April 5, 2025 – Saturday</h4>
  <ul>
    <li>6:00 AM – Adhikara Nandi procession</li>
    <li>9:00 PM – Procession on Bhootan and Bhutanki Vahanams</li>
  </ul>

  <h4>April 6, 2025 – Sunday</h4>
  <ul>
    <li>8:30 AM – Deity mounted on Purushamirugam and Singam (Lion) Vahanams</li>
    <li>9:00 PM – Night procession on Nagam and Kamadhenu mounts</li>
  </ul>

  <h4>April 7, 2025 – Monday</h4>
  <ul>
    <li>8:30 AM – Sowdal Vimanam procession</li>
    <li>Around 10:30 PM – Grand Vellividai Vahanam (Silver Rishabha Mount)</li>
  </ul>

  <h4>April 8, 2025 – Tuesday</h4>
  <ul>
    <li>Morning: Pallakku Vizha (Palanquin Procession)</li>
    <li>9:00 PM – Yaanai (Elephant) Vahanam procession</li>
  </ul>

  <h4>April 9, 2025 – Wednesday</h4>
  <ul>
    <li>6:00 AM – Thiruther Ezhuntharulal (Ratha Procession)</li>
    <li>7:15 AM – Pulling of the temple chariot (Ther Vadam Pidithal)</li>
    <li>Evening: Deity returns to the temple after chariot procession</li>
  </ul>

  <h4>April 10, 2025 – Thursday</h4>
  <ul>
    <li>Around 2:45 PM – Arubathu Moovar Utsavam (Procession of 63 Nayanmars) on Velli Vimanam</li>
    <li>Night – Sri Chandrasekarar's Paar Vettai Utsavam</li>
  </ul>

  <h4>April 11, 2025 – Friday</h4>
  <ul>
    <li>Morning – Pancha Moorthi Utsavam</li>
    <li>Evening – Around 6:00 PM – Bikshadanar Darshan (Lord Shiva as a mendicant)</li>
  </ul>

  <h4>April 12, 2025 – Saturday</h4>
  <ul>
    <li>Morning – Thirukoothan (Sri Nataraja) Darshan</li>
    <li>6:00 PM – Divine Siva Puja under the Punnai Tree in Mayil Form</li>
    <li>Around 7:30 PM – Thirukalyanam (Celestial Wedding)</li>
    <li>Festivities: Kailaya Voorthi, Flag Lowering (Kodiyirakkam), and Chandikeshwarar Utsavam</li>
  </ul>

  <h4>April 13, 2025 – Sunday</h4>
  <ul>
    <li>Morning – Uma Maheshwarar Darisanam</li>
    <li>Evening – Pandam Pari Vizha (Lighting Ceremony)</li>
  </ul>

  <p><strong>Vidaiyatri Festival</strong> (Farewell Processions) will continue for 10 days from April 14 (Monday) to April 23 (Wednesday), 2025.</p>

