    <h2>Amazon strengthens presence in Tamil Nadu with a new office building</h2>
      <section class="news">

      <h5>OMR News, Mar 30, 2022</h5>
      <div><img src="../WTC-Amazon.jpg" style="max-width: 720px;" class="responsive"></div alt="WTC Amazon">
      <p>Occupying 18 floors in the World Trade Centre, the new office is Amazon’s fourth in Tamil Nadu and can support around 6,000 employees.</p>
      <p>The new office will accommodate employees working in Amazon web services, technology support, engineering, devices, including the team responsible for the company’s most significant innovation - Echo - will work out of this new office, said Vinod Mathews, Director - Global Real Estate & Facilities, Amazon.</p>
      <p>The company’s single-minded focus from the beginning has always been working backwards from the customers and innovating on their behalf. To support this, a significant portion of the investment goes in technology, logistics and infrastructure to fulfil the expectation of customers, he said.</p>
      <p style="font-weight: bold; font-size:1.5em">job Opportunities for Localites</p>
      <p>As e-commerce, IT and ITeS continue to be one of the key drivers of job creation, with the absorption of both skilled and non-skilled workforce, the company’s key focus will be to create additional jobs over the coming years across all levels right from graduates to senior leaders, he said.

Mathews reiterated the company’s earlier announcement at the end of last year of creating 20 lakh direct and indirect jobs in India by 2025.

Amazon first began operations in Tamil Nadu with 50 people in Chennai in 2005. Today, with over 14,000 employees in the State, Tamil Nadu has emerged as a significant location for Amazon’s people and business strategy. Several technology, engineering, devices and operations teams operate from the company’s offices across Chennai. They have contributed in developing some of Amazon’s notable innovations across Amazon Devices.

Amazon has made significant investments in fulfilment infrastructure in the state. The company has four fulfilment centres and three sorting centres, enabling work opportunities for tens of thousands of youth in the State, says a company release.</p>

