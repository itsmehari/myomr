  <p>Residents including Elanchezian, Ilamathi, Jayaraj, Venkatraj, and Anbuchezian were acknowledged for their support and encouragement, reinforcing the role of community participation in such efforts.</p>
  <p>With rapid urbanisation reducing access to natural water sources across OMR, such initiatives are becoming essential. Birds and small animals are increasingly dependent on human-created water points during peak summer months.</p>

