  <p>As Chennai faces rising summer temperatures, residents of Plaza Opulance, a small and peaceful apartment community along OMR, have taken a simple yet impactful step to support birds and stray animals.</p>
  <p>Members of the Plaza Opulance Residence Association have installed cemented water bowls along their compound walls, ensuring easy access to drinking water for crows, pigeons, squirrels, cats, and other small animals that struggle to survive the heat.</p>
  <p>The initiative was supported with materials sourced from Water For Voiceless (WFV), an organisation that promotes grassroots efforts to care for animals during extreme weather conditions. Residents have taken responsibility to refill and maintain these water bowls daily.</p>
  <p>What stands out is not the scale, but the intent. A small group of residents came together, identified a local problem, and acted on it immediately without waiting for larger systems or external interventions.</p>

  <img
    class="article-inline-image"
